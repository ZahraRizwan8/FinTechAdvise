    private void b1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b1ActionPerformed
    String username = t1.getText();
    String pass = new String(t2.getPassword());
    String email = t3.getText();
    String contact = t4.getText();

    // Check if any of the fields are empty
    if (username.isEmpty() || pass.isEmpty() || email.isEmpty() || contact.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all the fields.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Don't proceed further if any field is empty
    }

    // Password validation
    if (!pass.matches("^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z]).{8,}$")) {
        JOptionPane.showMessageDialog(this, "Password must be at least 8 characters long and contain at least one uppercase letter, one digit.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Don't proceed further if password doesn't meet requirements
    }

    // Email validation
    if (!email.matches("^(.+)@(gmail\\.com|nu\\.edu\\.pk|hotmail\\.com)$")) {
        JOptionPane.showMessageDialog(this, "Invalid email format. Please use a valid email from gmail.com, nu.edu.pk, or hotmail.com.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Don't proceed further if email doesn't meet requirements
    }

    // Contact number validation
    if (!contact.matches("^\\d{11}$")) {
        JOptionPane.showMessageDialog(this, "Contact number must be 11 digits long.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Don't proceed further if contact number doesn't meet requirements
    }

    try {
        Connection conn = getConnection();
        String sql = "INSERT INTO signup (username, password, email, contact) VALUES (?, ?, ?, ?)";
        PreparedStatement statement = conn.prepareStatement(sql);
        statement.setString(1, username);
        statement.setString(2, pass);
        statement.setString(3, email);
        statement.setString(4, contact);
        statement.executeUpdate();
        conn.close();
        System.out.println("Data inserted successfully!");

        // After successful signup, redirect to Dashboard
        Dashboard dashboard = new Dashboard();
        dashboard.setVisible(true);
        dispose(); // Close the current SignupForm
    } catch (SQLException e) {
        System.err.println("Error inserting data: " + e.getMessage());
    }
    }//GEN-LAST:event_b1ActionPerformed

                   

