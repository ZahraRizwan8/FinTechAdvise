public String fetchUserInformation() {
    StringBuilder userInfo = new StringBuilder("<html>");
    try {
        Connection conn = getConnection();
        String sql = "SELECT * FROM signup";
        PreparedStatement statement = conn.prepareStatement(sql);
        ResultSet resultSet = statement.executeQuery();

        // Process the result set
        while (resultSet.next()) {
            String username = resultSet.getString("username");
            String password = resultSet.getString("password");
            String email = resultSet.getString("email");
            String contact = resultSet.getString("contact");

            // Append user information to the StringBuilder with HTML formatting
            userInfo.append("Username: ").append(username).append(", Password: ").append(password).append(", Email: ").append(email).append(", Contact: ").append(contact).append("<br>");
        }

        conn.close();
    } catch (SQLException e) {
        System.err.println("Error fetching user information: " + e.getMessage());
    }
    userInfo.append("</html>");
    return userInfo.toString();
}

